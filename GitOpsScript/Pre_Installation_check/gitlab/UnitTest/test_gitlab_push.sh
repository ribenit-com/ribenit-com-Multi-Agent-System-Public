#!/bin/bash
# 指定脚本使用 bash 解释器执行

set -euo pipefail
# -e  : 任意命令失败立即退出
# -u  : 使用未定义变量时报错 
# -o pipefail : 管道中任一命令失败都会导致整体失败
# 这是企业级脚本的安全标准写法

############################################
# 基础信息
############################################

SCRIPT_TO_TEST="./test_gitlab_push.sh"
# 指定要被测试的脚本路径

LOG_FILE="ci_git_test_$(date +%Y%m%d_%H%M%S).log"
# 生成日志文件名，带时间戳，避免覆盖历史日志

PASS_COUNT=0
# 记录通过测试数量

FAIL_COUNT=0
# 记录失败测试数量

############################################
# 日志系统
############################################

log() {
    # 定义日志函数
    # $1 表示传入的第一个参数
    echo -e "[$(date +%H:%M:%S)] $1" | tee -a "$LOG_FILE"
    # 输出带时间的日志
    # tee -a 表示同时输出到屏幕并追加到日志文件
}

pass() {
    # 测试通过时调用
    log "✅ $1"
    # 输出通过信息
    ((PASS_COUNT++))
    # 通过计数加 1
}

fail() {
    # 测试失败时调用
    log "❌ $1"
    # 输出失败信息
    ((FAIL_COUNT++))
    # 失败计数加 1
    exit 1
    # 立即退出整个脚本
}

section() {
    # 用于打印分段标题
    log "\n=============================="
    log "🔹 $1"
    log "=============================="
}

############################################
# 创建隔离环境
############################################

TEST_ROOT=$(mktemp -d)
# 创建临时目录，返回路径
# 例如 /tmp/tmp.xxxxxx

trap 'rm -rf "$TEST_ROOT"' EXIT
# 无论脚本正常结束还是异常退出
# 都自动删除整个测试目录，防止残留垃圾文件

LOCAL_REPO="$TEST_ROOT/local"
# 本地仓库路径

REMOTE_REPO="$TEST_ROOT/remote.git"
# 远程仓库路径（裸仓库）

CLONE_REPO="$TEST_ROOT/clone_check"
# 用于验证的 clone 仓库路径

mkdir "$LOCAL_REPO"
# 创建本地仓库目录

git init --bare "$REMOTE_REPO" >> "$LOG_FILE" 2>&1
# 创建裸仓库（模拟远程仓库）
# >> 表示追加到日志
# 2>&1 表示把错误输出也重定向到日志

pass "远程裸仓库创建成功"
# 标记远程仓库创建成功

############################################
# 测试 1：生产模式完整链路
############################################

section "测试 1：完整 push 流程"
# 打印测试标题

echo "hello CI" > "$LOCAL_REPO/test.txt"
# 在本地仓库创建一个测试文件

"$SCRIPT_TO_TEST" \
    "$LOCAL_REPO" \
    "$REMOTE_REPO" \
    master >> "$LOG_FILE" 2>&1 || fail "生产模式执行失败"
# 执行被测试脚本
# 参数：
#   本地目录
#   远程仓库路径
#   分支名 master
# 如果执行失败，调用 fail

pass "脚本执行成功"
# 标记脚本执行成功

git clone "$REMOTE_REPO" "$CLONE_REPO" >> "$LOG_FILE" 2>&1
# 从远程仓库克隆一份用于验证

if [ -f "$CLONE_REPO/test.txt" ]; then
    # 判断 test.txt 是否存在
    pass "远程仓库内容验证成功"
    # 存在表示 push 成功
else
    fail "远程仓库未包含 test.txt"
    # 不存在表示 push 失败
fi

############################################
# 测试 2：无变更场景
############################################

section "测试 2：无变更测试"
# 打印测试标题

"$SCRIPT_TO_TEST" \
    "$LOCAL_REPO" \
    "$REMOTE_REPO" \
    master >> "$LOG_FILE" 2>&1 || fail "无变更执行失败"
# 再执行一次脚本
# 这时没有新增文件
# 测试脚本是否正确处理“无变更”情况

pass "无变更处理正常"
# 如果没有报错说明处理正常

############################################
# 测试 3：错误远程地址测试
############################################

section "测试 3：错误远程测试"
# 打印测试标题

if "$SCRIPT_TO_TEST" \
    "$LOCAL_REPO" \
    "/invalid/remote/path.git" \
    master >> "$LOG_FILE" 2>&1; then
    # 如果错误远程竟然成功
    fail "错误远程未触发失败"
else
    # 正常应该进入这里
    pass "错误远程正确触发失败"
fi

############################################
# 测试总结
############################################

section "测试总结"
# 打印总结标题

log "通过: $PASS_COUNT"
# 输出通过数量

log "失败: $FAIL_COUNT"
# 输出失败数量

if [ "$FAIL_COUNT" -eq 0 ]; then
    # 如果失败为 0
    log "🎉 所有测试通过"
else
    # 否则说明存在问题
    log "⚠ 存在失败项"
fi
