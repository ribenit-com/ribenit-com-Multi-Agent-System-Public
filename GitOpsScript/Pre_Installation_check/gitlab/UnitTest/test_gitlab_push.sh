#!/bin/bash
# 指定脚本使用 bash 解释器执行

# ===============================================
# 通用 Git 上传脚本（测试 + 生产 双模式）
# ===============================================

set -euo pipefail
# -e  ：任何命令执行失败，脚本立即退出
# -u  ：使用未定义变量时报错
# -o pipefail ：管道中任意命令失败都会导致整体失败
# 👉 这是生产级脚本必须开启的安全选项

##############################################
# 参数处理
##############################################

if [[ $# -ge 2 ]]; then
    # 如果传入参数数量 >= 2
    # 表示使用“生产模式”
    
    LOCAL_DIR="$1"
    # 第一个参数：本地仓库目录路径

    REMOTE_URL="$2"
    # 第二个参数：远程仓库地址

    BRANCH="${3:-master}"
    # 第三个参数：分支名
    # 如果没有传，则默认使用 master

else
    # 如果没有传参数
    # 进入“单元测试兼容模式”

    echo "🔹 未传参数，进入测试兼容模式"

    LOCAL_DIR="$(pwd)"
    # 使用当前所在目录作为仓库目录

    REMOTE_URL=""
    # 不设置远程地址（测试脚本通常已提前配置好）

    if git rev-parse --is-inside-work-tree >/dev/null 2>&1; then
        # 检查当前目录是否是 Git 仓库

        BRANCH="$(git branch --show-current 2>/dev/null || echo master)"
        # 获取当前分支名
        # 如果失败则默认 master

    else
        BRANCH="master"
        # 如果不是 Git 仓库，则默认 master
    fi
fi

##############################################
# 进入目录
##############################################

echo "🔹 进入目录: $LOCAL_DIR"
# 打印当前进入的目录

cd "$LOCAL_DIR"
# 切换到指定目录

##############################################
# 初始化仓库（如果不存在）
##############################################

if [ ! -d ".git" ]; then
    # 如果当前目录没有 .git 文件夹

    echo "🔹 初始化 Git 仓库"

    git init
    # 初始化一个新的 Git 仓库
fi

##############################################
# 设置远程（如果传了 remote）
##############################################

if [[ -n "${REMOTE_URL:-}" ]]; then
    # 如果 REMOTE_URL 不为空

    if git remote | grep -q origin; then
        # 如果已经存在名为 origin 的远程仓库

        echo "🔹 更新远程地址"

        git remote set-url origin "$REMOTE_URL"
        # 更新 origin 地址

    else
        # 如果不存在 origin

        echo "🔹 添加远程仓库"

        git remote add origin "$REMOTE_URL"
        # 添加新的远程仓库
    fi
fi

##############################################
# 添加文件
##############################################

echo "🔹 添加文件"

git add .
# 将当前目录所有变更加入暂存区

##############################################
# 提交（如果有变更）
##############################################

if git diff --cached --quiet; then
    # 如果暂存区没有变化

    echo "⚠️ 没有变更可提交"

else
    # 如果存在变更

    echo "🔹 提交变更"

    git commit -m "auto commit"
    # 自动生成提交信息
fi

##############################################
# 推送（如果有 remote）
##############################################

if git remote | grep -q origin; then
    # 如果存在远程仓库 origin

    echo "🔹 推送到分支: $BRANCH"

    git push -u origin "$BRANCH"
    # 推送到远程分支
    # -u 表示建立 upstream 关联

else
    # 如果没有配置远程仓库

    echo "⚠️ 未配置 remote，跳过 push"
fi

echo "✅ upload.sh 执行完成"
# 输出执行完成提示
